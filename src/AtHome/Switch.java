package AtHome;

public class Switch {

	public static void main(String[] args) {
		
		int grade = 1;
		
		switch(grade) { // Must give an Expression: data
		case 1:
			System.out.println("1");
			
	
		case 2:
			System.out.println("2");
			  // closing statement
		
		break;
			
		default:
			System.out.println("Invalid");
			break;
	}
	}
}
