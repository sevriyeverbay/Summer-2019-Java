package Day03_Variables;

public class EscapeSequences {
/*

\n (or \r): starts from a new line
\t : horizontal tab
\\: prints a backslash                        /*

\': prints single quote
\": prints double quote
\: backslash	
/:front slash

*/
	public static void main(String[] args) {
	System.out.print("Hello Cybertek\n Batch 12 students \n\t sevriye \n");
	System.out.println("Tomorrow is off\n");
	System.out.println("True\r");
	System.out.println("False\n\n\n");
	System.out.println("\tClass starts tomorrow");
	System.out.println("\\muhtar");
	System.out.println("\'java");
	System.out.println("Book is called \"Game of Thrones\"");
	
	}

}

